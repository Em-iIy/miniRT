#include "vect3_math.h"

t_vect3	vect3_cross_product(t_vect3 a, t_vect3 b)
{
	return ((t_vect3){
		a[1] * b[2] - a[2] * b[1],
		a[2] * b[0] - a[0] * b[2],
		a[0] * b[1] - a[1] * b[0]
	});
}
